public class Bitwise  
{   
public static void main(String[] args)   
{   
  // bitwise OR
  int x = 9, y = 8;    
  // 1001 & 1000 = 1000 = 8  
  System.out.println("x & y = " + (x & y));   
  // bitwise XOR
  int x = 9, y = 8;   
  // 1001 ^ 1000 = 0001 = 1  
  System.out.println("x ^ y = " + (x ^ y));
  // bitwise OR
  int x = 9, y = 8;   
  // 1001 | 1000 = 1001 = 9  
  System.out.println("x | y = " + (x | y));  
    
}  
} 
